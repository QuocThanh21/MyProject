const { LoginPage } = require("../pages/login-page")

// -- This is a parent command --
Cypress.Commands.add('login', (username, password) => { 
    LoginPage.inputUsername(username)
    LoginPage.inputPassword(password)
    LoginPage.clickLoginBtn()   
 })
